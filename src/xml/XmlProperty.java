package xml;

public class XmlProperty {

    private String name;
    private String value;

    public XmlProperty() {
    }

    public String getName() {
    return name;
  }
  public void setName(String name) {
    this.name = name;
  }
  public void setValue(String value) {
    this.value = value;
  }
  public String getValue() {
    return value;
  }

}
